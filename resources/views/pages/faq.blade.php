<h4><u>How do I sign up?</u></h4>

<p>If you want to sign up then you need to register a new account. In order to do this you need to click the
   'Register' Link on the login page. This will direct you to a new page which will allow you to create a new account
   once you have completed the required fields. Which are email and password. </p>

<hr>

<h4><u>How do I change my password?</u></h4>

<p>If you want to change your password then all you need to do click on the 'settings' navigation link
    which is situated in the top right corner. From there you will see the option were you can change your
    password.</p>

<hr>

<h4><u>Can I delete a poll?</u></h4>

<p>Yes you can, all you need to do is when you are on the homepage you will see the list of both the closed
    and active polls. Each poll will have a list of commands next to them with one being the option to remove
    the poll permanently.</p>

